    private void refNumTextFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refNumTextFActionPerformed
        try {
            int test = Integer.parseInt(refNumTextF.getText());
            if(productList.checkEqualRefNum(test)) {
                refNumLabel.setVisible(true);
                allGood[6] = false;
                addedRefNum.setVisible(allGood[6]);
                return;
            }
            else {
                refNumLabel.setVisible(false);
            }
            objArr[6] = test;
            allGood[6] = true;
            checkAllGood();
        }
        catch (NumberFormatException e) {           
            allGood[6] = false;
        }
        addedRefNum.setVisible(allGood[6]);
        invalidRefNumLabel.setVisible(!allGood[6]);
    }//GEN-LAST:event_refNumTextFActionPerformed

    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        productList.remove(oldRefNum);
        productList.add(new Product(objArr));
        inventoryList.deleteRow(oldRefNum);
        inventoryList.addTableRow(objArr);
        this.dispose();
    }//GEN-LAST:event_confirmButtonActionPerformed

