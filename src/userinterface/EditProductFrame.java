    private void priceTextFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_priceTextFActionPerformed
        try {
            double test = Double.parseDouble(priceTextF.getText());
            if(test<0)
                throw new NumberFormatException();
            objArr[3] = test;
            allGood[3] = true;
            checkAllGood();
        }
        catch (NumberFormatException e) {
            allGood[3] = false;
        }
        addedPrice.setVisible(allGood[3]);
        invalidPriceLabel.setVisible(!allGood[3]);
    }//GEN-LAST:event_priceTextFActionPerformed

    private void qtyTextFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qtyTextFActionPerformed
        try {
            int test = Integer.parseInt(qtyTextF.getText());
            if(test<0)
                throw new NumberFormatException();
            if(test>10)
                statusTextF.setText("Available");
            else if(test>0)
                statusTextF.setText("Running Out");
            else
                statusTextF.setText("Empty");
            objArr[4] = test;
            allGood[4] = true;
            checkAllGood();
        }
        catch (NumberFormatException e) {          
            statusTextF.setText("");
            allGood[4] = false;
        }
        addedQty.setVisible(allGood[4]);
        invalidQtyLabel.setVisible(!allGood[4]);
    }//GEN-LAST:event_qtyTextFActionPerformed

